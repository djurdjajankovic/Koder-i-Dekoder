void oduzmi(int d[],int n)
{
    for(int i=0;i<n;i++)
    {
        d[i]=((d[i]-2*i)/3);
    }
}
void dekadniUBinarni(int **p,int d[],int n)
{
    for(int i=0;i<n;i++)
    {
        int k=5;
        while(d[i]>0)
        {
            p[k][i]=d[i]%2;
            d[i]=d[i]/2;
            k--;
        }
    }
}
void pomeriUDesno(int **p, int n)
{
    int k,i,j,t,m;
    for(i=0;i<n;i++)
    {
        k=0;
        for(j=0;j<6;j++)
        {
            if(p[j][i]==1)
            {
                k++;
            }
        }
        for(j=0;j<k;j++)
        {
            t=p[5][i];
            for(m=5;m>0;m--)
            {
                p[m][i]=p[m-1][i];
            }
            p[0][i]=t;
        }
    }
}
